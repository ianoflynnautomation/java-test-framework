package solutions.bjjeire.core.data.common;

/**
 * Represents the days of the week.
 * The default serialization with WRITE_ENUMS_USING_INDEX will produce
 * 0 for SUNDAY, 1 for MONDAY, etc., which is a common convention.
 */
public enum DayOfWeek {
    SUNDAY,
    MONDAY,
    TUESDAY,
    WEDNESDAY,
    THURSDAY,
    FRIDAY,
    SATURDAY
}